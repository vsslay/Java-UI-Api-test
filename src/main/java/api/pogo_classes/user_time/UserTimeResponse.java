package api.pogo_classes.user_time;

public class UserTimeResponse {
    private String name;
    private String job;
    private String updatedAt;

    public UserTimeResponse() {
    }

    public String getName() {
        return name;
    }

    public String getJob() {
        return job;
    }

    public String getUpdatedAt() {
        return updatedAt;
    }
}
